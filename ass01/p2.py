#WAP to find the volume of the cuboid
l= eval(input("enter the length of the cuboid :"))
b= eval(input("enter the breath of the cuboid :"))
h= eval(input("enter the height of the cuboid :"))
volume = l*b*h
area = 2*(l*b+b*h+h*l)
print("length" , l , "breath" , b , "height" , h , "volume"  , "=" , volume ) 
print("length" , l , "breath" , b , "height" , h , "surface area of cuboid"  , "=" , area ) 
